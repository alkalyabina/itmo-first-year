select Н_ЛЮДИ.ФАМИЛИЯ, Н_СЕССИЯ.ДАТА from Н_ЛЮДИ
  inner join Н_СЕССИЯ on Н_СЕССИЯ.ЧЛВК_ИД = Н_ЛЮДИ.ИД
  where Н_ЛЮДИ.ФАМИЛИЯ < 'Петров' and Н_СЕССИЯ.ЧЛВК_ИД < 106059;

-- 2.

select Н_ЛЮДИ.ОТЧЕСТВО, Н_ОБУЧЕНИЯ.ЧЛВК_ИД, Н_УЧЕНИКИ.НАЧАЛО from Н_ЛЮДИ
  inner join Н_ОБУЧЕНИЯ on Н_ОБУЧЕНИЯ.ЧЛВК_ИД = Н_ЛЮДИ.ИД
  inner join Н_УЧЕНИКИ on Н_УЧЕНИКИ.ЧЛВК_ИД = Н_ЛЮДИ.ИД
  where Н_ЛЮДИ.ИД < 142095 and Н_ОБУЧЕНИЯ.НЗК > '001000' and Н_УЧЕНИКИ.ГРУППА < '1101';